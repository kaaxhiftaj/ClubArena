public class BlogModel {

    private String blogId;
    private String blogTitle;
    private String blogLink;
    private String blogImageLink;
    private String blogDes;

    public String getBlogId() {
        return blogId;
    }

    public void setBlogId(String blogId) {
        this.blogId = blogId;
    }

    public String getBlogTitle() {
        return blogTitle;
    }

    public void setBlogTitle(String blogTitle) {
        this.blogTitle = blogTitle;
    }

    public String getBlogLink() {
        return blogLink;
    }

    public void setBlogLink(String blogLink) {
        this.blogLink = blogLink;
    }

    public String getBlogImageLink() {
        return blogImageLink;
    }

    public void setBlogImageLink(String blogImageLink) {
        this.blogImageLink = blogImageLink;
    }

    public String getBlogDes() {
        return blogDes;
    }

    public void setBlogDes(String blogDes) {
        this.blogDes = blogDes;
    }



}
